<?php

$a = 55.0;

$b = 55;

//marior que
var_dump($a > $b);

echo "<br>";

//menor que
var_dump($a < $b);

echo "<br>";

//comparação de valor
var_dump($a == $b);

echo "<br>";

//comparação de valor e tipo (igualdade de identidade)
var_dump($a === $b);

echo "<br>";

//negação ("!" antes da chave converte para negativo)
var_dump($a != $b);

echo "<br>";

//negação de valor de tipo (negação de igualdade de identidade)
var_dump($a !== $b);

?>