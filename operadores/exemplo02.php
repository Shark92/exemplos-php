<?php

$valorTotal = 0;

//usando o operador de atribuição (+) para adicionar mais um valor de numeral a uma variável q já possui um valor numérico
$valorTotal += 100;

$valorTotal += 25;

//usando o operador de atribuição (*) para multiplicar um certo valor de numeral de uma variável q já possui um valor numérico (neste exemplo, a variável está sendo multiplicada por "0.9" para ser feito uma subtração de porcentagem.)
$valorTotal *= .9;

//usando o operador de atribuição (-) para subtrair um certo valor de numeral de uma variável q já possui um valor numérico
$valorTotal -= 1;

echo $valorTotal;

?>